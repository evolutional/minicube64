void mfb_putchv(struct mfb_window *window,unsigned short c,int x, int y,uint32_t rgba)
{
	int i, j;
	if(c < 32 || c > 127) c = 127;
	for(j = 0; j < 8 ; j++)
	{
		unsigned char bits = vincent_data[c][j];
		for(i = 0; i < 8 ; i++)
		{
			if( bits & (1 << i)!=0)
				mfb_setpix(window,x+i,y+j,rgba);
		}
	}
}


void mfb_print(struct mfb_window *window,int x, int y, uint32_t rgba, const char *text, ...)
{
static int stb_fontinit=0;
	char tmp[1024];
	va_list args;
	const char *p;
	int start = x, c;
	// Expand the formatting string.
	va_start(args, text);
	vsnprintf(tmp, sizeof(tmp), text, args);
	tmp[sizeof(tmp)-1] = 0;
	va_end(args);

	// Print each glyph.
	p = tmp;
	while (*p)
	{
		mfb_putchv(window,*p,x,y,rgba);
		x+=8;
		p++;
	}
}

