void mfb_putch(unsigned short c,int x, int y,uint32_t rgba)
{
		int frow, fcol, byte;
    int i, j;
    if(c < 32 || c > 127) c = 127;
    c -= 0x20;
    fcol = c >> 3;
    int shf = (fcol & 0x1);
    fcol >>= 1;    
    frow = c & 0x7;    
    byte = frow * 5 * 6 + fcol;
    for(j = 0; j < 5 ; j++)
		{
				unsigned char bits = mfb_tom_thumb_bits[byte];
				if(shf) bits >>= 4;
				for(i = 0; i < 4 ; i++)
				{
					if(!(bits & (1 << i)))
					{
						mfb_setpix(x+i,y+j+1,0xff000000);
						mfb_setpix(x+i,y+j,rgba);
					}
				}
        byte += 6;
    }
}

void mfb_print(int x, int y, uint32_t rgba, const char *text, ...)
{
static int stb_fontinit=0;
	char tmp[1024];
	va_list args;
	const char *p;
	int start = x, c;
	// Expand the formatting string.
	va_start(args, text);
	vsnprintf(tmp, sizeof(tmp), text, args);
	tmp[sizeof(tmp)-1] = 0;
	va_end(args);

	// Print each glyph.
	p = tmp;
	while (*p)
	{
		mfb_putch(*p,x,y,rgba);
		x+=4;
		p++;
	}
}


